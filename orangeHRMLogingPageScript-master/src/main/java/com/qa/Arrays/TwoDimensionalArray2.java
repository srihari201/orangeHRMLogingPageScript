package com.qa.Arrays;

public class TwoDimensionalArray2 {
    public static void main(java.lang.String[]args){
        java.lang.System.out.println("Execution will be started from main method");
        int[][] a={{1,2},{2,3,4}};

        java.lang.System.out.println(a.length);
        java.lang.System.out.println(a[1].length);
        java.lang.System.out.println(a[0][1]);
        java.lang.System.out.println(a[1][2]);




        java.lang.System.out.println("Execution will be ended");
    }
}
