package com.programs;

public class SwappingOfNumbers {
    public static void main(String[] args){
        int a=10;
        int b=20;
        System.out.println("Before sorting The value of a : " + a + " and the value of b: " +b );
        int c=a;//10
        a=b;//20
        b=c;//10
        System.out.println("After sorting The value of a : " + a + " and the value of b: " +b );
    }
}
